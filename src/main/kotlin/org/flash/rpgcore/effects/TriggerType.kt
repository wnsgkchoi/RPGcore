package org.flash.rpgcore.effects

/**
 * 게임 내에서 효과(Effect)를 발동시킬 수 있는 모든 종류의 이벤트를 정의합니다.
 * 실제 장비 및 스킬 YAML 파일에 정의된 트리거와 향후 확장성을 고려하여 작성되었습니다.
 */
enum class TriggerType {
    /*======================================
    * 전투 관련 트리거
    ======================================*/
    ON_HIT_DEALT,
    ON_HIT_TAKEN,
    ON_CRIT_DEALT,
    ON_KILL,
    ON_DODGE,
    ON_DEATH,

    /*======================================
    * 스킬/아이템 관련 트리거
    ======================================*/
    ON_SKILL_USE,
    ON_CONSUME_ITEM,
    ON_PROJECTILE_LAUNCH,
    ON_PROJECTILE_HIT,

    /*======================================
    * 플레이어 상태 및 행동 관련 트리거
    ======================================*/
    ON_MOVE,
    PERIODIC,
    ON_HEALTH_THRESHOLD,
    ON_STATUS_EFFECT_APPLIED,

    /*======================================
    * 영구/조건부 효과 등록용 트리거
    ======================================*/
    /** 장비를 착용하거나 해제하는 '순간'에 발동하는 일회성 효과에 사용됩니다. */
    ON_EQUIP,

    /**
     * 스킬/장비가 활성화된 동안, 스탯 계산 시 항시 적용되는 효과를 등록하기 위한 트리거입니다.
     * 실제 이벤트가 발생하지 않으며, StatManager가 스탯을 계산할 때 이 타입의 효과를 참고합니다.
     * (예: 강체, 반사 오라 등)
     */
    PASSIVE_STAT_MODIFIER
}